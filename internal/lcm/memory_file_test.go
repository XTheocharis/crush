func TestLoadMemoryFile_RoundTrip(t *testing.T) {
	t.Parallel()

	dir := t.TempDir()
	original := []ExtractedMemory{
		{Type: MemoryFact, Content: "The project uses SQLite via sqlc", Confidence: 0.95},
		{Type: MemoryDecision, Content: "Chose sha256 for ID generation", Confidence: 0.9},
		{Type: MemoryLesson, Content: "Tree-sitter requires CGO_ENABLED=1", Confidence: 0.85},
	}

	_, err := WriteMemoryFile(context.Background(), original, dir, MemoryFileConfig{})
	require.NoError(t, err)

	loaded, err := LoadMemoryFile(dir)
	require.NoError(t, err)
	require.Len(t, loaded, 3)

	require.Equal(t, MemoryFact, loaded[0].Type)
	require.Equal(t, "The project uses SQLite via sqlc", loaded[0].Content)
	require.InDelta(t, 0.5, loaded[0].Confidence, 0.01)

	require.Equal(t, MemoryDecision, loaded[1].Type)
	require.Equal(t, "Chose sha256 for ID generation", loaded[1].Content)

	require.Equal(t, MemoryLesson, loaded[2].Type)
	require.Equal(t, "Tree-sitter requires CGO_ENABLED=1", loaded[2].Content)
}

