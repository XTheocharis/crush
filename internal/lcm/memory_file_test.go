func TestWriteMemoryFile_BasicOutput(t *testing.T) {
	t.Parallel()

	dir := t.TempDir()
	memories := []ExtractedMemory{
		{Type: MemoryFact, Content: "The project uses SQLite via sqlc", Confidence: 0.95},
		{Type: MemoryDecision, Content: "Chose sha256 for ID generation", Confidence: 0.9},
	}

	n, err := WriteMemoryFile(context.Background(), memories, dir, MemoryFileConfig{})
	require.NoError(t, err)
	require.Greater(t, n, 0)

	data, err := os.ReadFile(filepath.Join(dir, "CRUSH.memory.md"))
	require.NoError(t, err)
	content := string(data)

	require.Contains(t, content, "# Session Memory")
	require.Contains(t, content, "<!-- auto-generated by Crush -->")
	require.Contains(t, content, "**[fact]** The project uses SQLite via sqlc")
	require.Contains(t, content, "**[decision]** Chose sha256 for ID generation")
}

func TestWriteMemoryFile_LineLimitEnforced(t *testing.T) {
	t.Parallel()

	dir := t.TempDir()
	var memories []ExtractedMemory
	for i := range 250 {
		memories = append(memories, ExtractedMemory{
			Type:       MemoryFact,
			Content:    "item " + itoa(i),
			Confidence: 0.9,
		})
	}

	n, err := WriteMemoryFile(context.Background(), memories, dir, MemoryFileConfig{MaxLines: 50})
	require.NoError(t, err)
	require.Greater(t, n, 0)

	data, err := os.ReadFile(filepath.Join(dir, "CRUSH.memory.md"))
	require.NoError(t, err)
	lines := strings.Split(string(data), "\n")
	require.LessOrEqual(t, len(lines), 51) // 50 lines + truncation marker
	require.Contains(t, string(data), "... (truncated)")
}

