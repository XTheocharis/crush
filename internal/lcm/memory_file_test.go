func TestWriteMemoryFile_ManualMarkerSkips(t *testing.T) {
	t.Parallel()

	dir := t.TempDir()
	existing := "# My Notes\n\n<!-- manual -->\n\nSome hand-written content.\n"
	require.NoError(t, os.WriteFile(filepath.Join(dir, "CRUSH.memory.md"), []byte(existing), 0o644))

	memories := []ExtractedMemory{
		{Type: MemoryFact, Content: "should not appear", Confidence: 0.9},
	}

	n, err := WriteMemoryFile(context.Background(), memories, dir, MemoryFileConfig{})
	require.NoError(t, err)
	require.Equal(t, 0, n)

	data, err := os.ReadFile(filepath.Join(dir, "CRUSH.memory.md"))
	require.NoError(t, err)
	require.Contains(t, string(data), "Some hand-written content")
	require.NotContains(t, string(data), "should not appear")
}

